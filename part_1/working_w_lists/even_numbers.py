#we can give arguments to the range() function besides start and finish. The third is known as 'step', which is the increment for the each next number in the range

even_numbers = list(range(2, 11, 2))
print(even_numbers)
# [ 2, 4, 6, 8, 10]

divisible_by_3 = list(range(3, 21, 3))
print(divisible_by_3)
# [3, 6, 9, 12, 15, 18]



